    private void cad_AlterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cad_AlterActionPerformed
        if (this.p_tipo.getSelectedIndex() == 0) {
            Cliente c = new ClientePF(Integer.parseInt(cli_id.getText()), cad_nome.getText(), cad_sobrenome.getText(), cad_tel.getText(), cad_email.getText(), cad_endr.getText(), cad_doc.getText());
            Agenda a = new Agenda();
            a.atualizar(c);
        }
        if (this.p_tipo.getSelectedIndex() == 1) {
            Cliente c = new ClientePJ(Integer.parseInt(cli_id.getText()), cad_nome.getText(), cad_sobrenome.getText(), cad_tel.getText(), cad_email.getText(), cad_endr.getText(), cad_doc.getText());
            Agenda a = new Agenda();
            a.atualizar(c);
        }
        AgendaView.preecherLista();
        this.setVisible(false);

    }//GEN-LAST:event_cad_AlterActionPerformed

    private void Cad_subMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Cad_subMouseClicked
        if (this.p_tipo.getSelectedIndex() == 0) {
            Cliente c = new ClientePF(cad_nome.getText(), cad_sobrenome.getText(), cad_tel.getText(), cad_email.getText(), cad_endr.getText(), cad_doc.getText());
            Agenda a = new Agenda();
            a.adicionar(c);
        }
        if (this.p_tipo.getSelectedIndex() == 1) {
            Cliente c = new ClientePJ(cad_nome.getText(), cad_sobrenome.getText(), cad_tel.getText(), cad_email.getText(), cad_endr.getText(), cad_doc.getText());
            Agenda a = new Agenda();
            a.adicionar(c);
        }
        AgendaView.preecherLista();
        this.setVisible(false);

    }//GEN-LAST:event_Cad_subMouseClicked

